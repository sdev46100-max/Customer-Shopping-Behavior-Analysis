#Q7 Segment customers into New, Returning, and Loyal based on there total number of previous purchases, and show the count of each segment.
with customer_type as(
select customer_id,previous_purchased, (case when previous_purchased=1 then 'New Customer'
                         when previous_purchased between 2 and 10 then 'Returning Customer'
                         else 'Loyal Customer'
                         end) as customer_segment
from customer_shopping_behavior)
select customer_segment,count(*)as no_of_customer
from customer_type
group by customer_segment;

#Q8 What are the top 3 most purchased products within each category?
with csb as (
select category,item_purchased,count(customer_id) as total_orders,
row_number() over (partition by category order by count(customer_id) desc)as item_rank
from customer_shopping_behavior
group by category,item_purchased)
select item_rank,category,item_purchased, total_orders
from csb
where item_rank <=3;

#Q9 Are customers who have repeat buyers(more than 5 previous purchases) also likey to subscribe?
