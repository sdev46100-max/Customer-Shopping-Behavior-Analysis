 #Q10 What is total revenue contribution of each age group?
 with csb as(
select age,sum(purchase_amt) as sum_revenue , case when age between 13 and 19 then "teenager"
                 when age between 20 and 39 then "young adult"
                 when age between 40 and 59 then "middle-aged adult"
                 else "senior citizen"
                 end as age_group
                 from customer_shopping_behavior
                 group by age)
                 
select age_group,sum(sum_revenue) as total_revenue
from csb
group by age_group
order by total_revenue desc;
 

